public class BancoForm {

    private Integer idBanco;
    private String sbanco;
    private String sbancobus;

    /**
     * @return the idBanco
     */
    public Integer getIdBanco() {
        return idBanco;
    }

    /**
     * @param idBanco the idBanco to set
     */
    public void setIdBanco(Integer idBanco) {
        this.idBanco = idBanco;
    }

    /**
     * @return the sbanco
     */
    public String getSbanco() {
        return sbanco;
    }

    /**
     * @param sbanco the sbanco to set
     */
    public void setSbanco(String sbanco) {
        this.sbanco = sbanco;
    }

    /**
     * @return the sbancobus
     */
    public String getSbancobus() {
        return sbancobus;
    }

    /**
     * @param sbancobus the sbancobus to set
     */
    public void setSbancobus(String sbancobus) {
        this.sbancobus = sbancobus;
    }
   
}
